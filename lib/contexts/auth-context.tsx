'use client'

import { createContext, useContext, useEffect, useState } from 'react'
import { createClient } from '@/lib/supabase/client'
import { User } from '@supabase/supabase-js'
import { useRouter } from 'next/navigation'

type AuthContextType = {
  user: User | null
  loading: boolean
  signIn: (email: string, password: string) => Promise<{ error: Error | null }>
  signUp: (email: string, password: string) => Promise<{ error: Error | null }>
  signOut: () => Promise<void>
}

const AuthContext = createContext<AuthContextType | undefined>(undefined)

export function AuthProvider({ children }: { children: React.ReactNode }) {
  const [user, setUser] = useState<User | null>(null)
  const [loading, setLoading] = useState(true)
  const router = useRouter()
  const supabase = createClient()

  useEffect(() => {
    // Check active sessions and sets the user
    supabase.auth.getSession().then(({ data: { session } }) => {
      setUser(session?.user ?? null)
      setLoading(false)
    })

    // Listen for changes on auth state (sign in, sign out, etc.)
    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((_event, session) => {
      setUser(session?.user ?? null)
      setLoading(false)
      router.refresh()
    })

    return () => subscription.unsubscribe()
  }, [router, supabase.auth])

  const signIn = async (email: string, password: string) => {
    const { error } = await supabase.auth.signInWithPassword({
      email,
      password,
    })
    return { error }
  }

  const signUp = async (email: string, password: string) => {
    const { error } = await supabase.auth.signUp({
      email,
      password,
    })
    return { error }
  }

  const signOut = async () => {
    try {
      // CRITICAL: Clear user session markers BEFORE signing out
      // This allows useUserStorage to detect user switches on next login
      localStorage.removeItem('pawkit_last_user_id');

      // Revoke extension token if one exists (prevents stale token use)
      try {
        await fetch('/api/extension/token', { method: 'DELETE' });
      } catch {
        // Non-critical - continue with logout anyway
      }

      // Sign out from Supabase
      await supabase.auth.signOut();

      // Close any open database connections
      // This allows fresh connections for next user
      try {
        const { localDb } = await import('@/lib/services/local-storage');
        const { syncQueue } = await import('@/lib/services/sync-queue');
        await localDb.close();
        await syncQueue.close();
      } catch (dbError) {
        // Non-critical - continue anyway
      }

      router.push('/login');
    } catch (error) {
      // Try to redirect anyway
      router.push('/login');
    }
  }

  return (
    <AuthContext.Provider value={{ user, loading, signIn, signUp, signOut }}>
      {children}
    </AuthContext.Provider>
  )
}

export function useAuth() {
  const context = useContext(AuthContext)
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider')
  }
  return context
}
